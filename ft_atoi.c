#include "ft_printf.h"

int	ft_atoi(const char *str)
{
	int				i;
	unsigned long	nbr;

	i = 0;
	nbr = 0;
	while ((str[i] >= '0' && str[i] <= '9') && str[i])
	{
		nbr = (nbr * 10) + (str[i] - 48);
		i++;
	}
	return (nbr);
}
